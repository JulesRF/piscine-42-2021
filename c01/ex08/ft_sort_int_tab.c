void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	a;
	int	j;

	i = 0;
	a = 0;
	while (i < size)
	{
		j = 0;
		while (j < size - 1)
		{
			if (tab[j] > tab[j + 1])
			{
				a = tab[j];
				tab[j] = tab[j + 1];
				tab[j + 1] = a;
			}
			j++;
		}
		i++;
	}
}
